/*------------------------------------------------------------------------------------------------------------------------
 * 函数名称：SComp

 * 函数功能：字符串比较

 * 输入参数：*a(原字符串)*b(比较字符串)

 * 输出参数：1:相同 0:不相同

 * 注    意：无
------------------------------------------------------------------------------------------------------------------------*/
u2 SComp(const char *a,const char *b)
{
	u8 i = 0;
	while(1)
	{
	 	if(*a == 0 && i > 0)
			return 1;
		if(*a != *b)
			return 0;	
		a++;
		b++;
		i++;
	}
}
/*------------------------------------------------------------------------------------------------------------------------
 * 函数名称：ScC

 * 函数功能：字符串小写转换大写

 * 输入参数：*a(字符串)

 * 输出参数：无

 * 注    意：无
------------------------------------------------------------------------------------------------------------------------*/
void ScC(char *a)
{
	while(1)
	{
		if(*a == 0)
			break;
		if(*a > 96 && *a < 123)
		{	
			*a -= 32;
		}
		a++;
	}
}
/*------------------------------------------------------------------------------------------------------------------------
 * 函数名称：SAa

 * 函数功能：字符串大写转换小写

 * 输入参数：*a(字符串)

 * 输出参数：无

 * 注    意：无
------------------------------------------------------------------------------------------------------------------------*/
void SAa_lookup(char *a,char sdata)
{
	while(1)
	{
		if(*a == 0 || *a == sdata)
			break;
		if(*a > 64 && *a < 91)
		{
			*a += 32;
		}
		a++;
	}	
}

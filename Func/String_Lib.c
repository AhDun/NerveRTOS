/*------------------------------------------------------------------------------------------------------------------------
 * 函数名称：SLookup

 * 函数功能：字符串，字符定位

 * 输入参数：*a(字符串)sdata(字符)

 * 输出参数：位置

 * 注    意：无
------------------------------------------------------------------------------------------------------------------------*/
u8 SLookup(const char *a,char sdata)
{
	u8 SL_F0 = 0;
	while(SL_F0 < 254)
	{
		if(*a == sdata)
			return SL_F0;
		SL_F0++;
		a++;
	}
	return SL_F0;
}
u32 ASCII_DEC(const char *a)
{
	u32 A_DEC = 0;
	while(1)
	{
		if(*a > 47 && *a < 58)
			A_DEC = (A_DEC * 10) + (*a  - 48); 
		else
			break;
		a++;
	}
	return A_DEC;
}

